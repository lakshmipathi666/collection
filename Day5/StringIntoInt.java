package Day5;

public class StringIntoInt {
	public static void main(String [] args) {
		String [] name= {"10","15","20","30"};
		int name1=Integer.parseInt(name[0]);
		int name2=Integer.parseInt(name[1]);
		int name3=Integer.parseInt(name[2]);
		int name4=Integer.parseInt(name[3]);
		
		int num=name1+name2+name3+name4;
		
		System.out.println("The value = "+num);
		
		
	}
	

}
