package Conditions;

public class Vowels {
	public static void main(String[] args) {
		String a="lachu";
		if(a.contains("a")||a.contains("e")||a.contains("i")||a.contains("o")||a.contains("u")) {
			System.out.println(a+" word contains vowels");
		
		}
		else {
			System.out.println( a+ " word not contains vowels ");
		}
	}
}
